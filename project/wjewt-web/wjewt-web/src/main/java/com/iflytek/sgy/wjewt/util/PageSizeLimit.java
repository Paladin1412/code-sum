package com.iflytek.sgy.wjewt.util;

/**
 * {分页每页最大条数限制}
 * 
 * @author xfzhu3
 * @created 2018年5月7日 上午10:19:12
 * @lastModified
 * @history
 */
public class PageSizeLimit {
	/**
	 * 分页最大条数限制
	 */
	static int maxPageSize = 100;

	/**
	 * ｛置顶分页条数大小限制｝
	 * 
	 * @param pageSize
	 *            分页条数
	 * @return int
	 * @author xfzhu3
	 * @created 2018年5月7日 上午10:20:25
	 * @lastModified
	 * @history
	 */
	public static int getPageSizeLimit(int pageSize) {
		if (pageSize > maxPageSize) {
			pageSize = maxPageSize;
		}
		return pageSize;
	}
}
