package com.iflytek.sgy.wjewt.services.base;


import java.util.Map;

/**
 * 推送 接收消息类
 *
 * Created by nanhuang on 16/2/17.
 */
public class PushMessage {

    /**
     * 是否有后续动作 默认无动作0  有1
     */
    private String isAction = "0";

    /**
     * 动作指定activity名称 全称
     */
    private String actionActivity;
    /**
     * 标题
     */
    private String title;

    /**
     * 内容
     */
    private String content;

    /**
     * 通知栏样式 默认样式0
     */
    private String notificationType = "0";

    /**
     * 打开方式 默认0  1为web方式
     */
    private String actionType = "0";

    /**
     * 业务类型 默认为空
     */
    private String businessType;

    /**
     * 参数 intent传入 参数
     */
    private Map<String,Object> actionParam;

    private String url;
    
    private String type;
    
    public String getIsAction() {
        return isAction;
    }

    public void setIsAction(String isAction) {
        this.isAction = isAction;
    }

    public String getActionActivity() {
        return actionActivity;
    }

    public void setActionActivity(String actionActivity) {
        this.actionActivity = actionActivity;
    }

    public String getActionType() {
        return actionType;
    }

    public void setActionType(String actionType) {
        this.actionType = actionType;
    }

    public String getBusinessType() {
        return businessType;
    }

    public void setBusinessType(String businessType) {
        this.businessType = businessType;
    }

    public Map<String, Object> getActionParam() {
        return actionParam;
    }

    public void setActionParam(Map<String, Object> actionParam) {
        this.actionParam = actionParam;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getNotificationType() {
        return notificationType;
    }

    public void setNotificationType(String notificationType) {
        this.notificationType = notificationType;
    }

	/**
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}

	/**
	 * @param url the url to set
	 */
	public void setUrl(String url) {
		this.url = url;
	}

	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}

	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
    
}
